# Pero hasta ahora solo hemos estado imprimiendo texto, ¿qué pasa si queremos iprimir numeros?

print("2")
print(2) 
# En este el print no devuelve error, pero imprime dos cosas diferentes
# No se nota la diferenca, pero la primera es un string y la segunda es un entero, eso quiere decir que con el primer print() se imprime un texto y con el segundo un numero
# Con el segundo podemos aplicar operaciones matematicas

# Suma
print(2 + 2)

# Resta
print(2 - 2)

# Multiplicación
print(2 * 3)

# División
print(2 / 3)

# División entera
print(2 // 3)

# Potencia
print(2 ** 3)

# Módulo, esto es el resto de la división
print(2 % 3)