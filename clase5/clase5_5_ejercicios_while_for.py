# Ejercicio 1: Cree un programa que le pida una clave al usuario, la clave debe contener al menos un 7 un 3 un 6  y una vocal. Si la clave cumple con los requisitos imprima 'Clave valida' en caso contrario imprima 'Clave invalida' y vuelva a pedir la clave

# Ejemplo:
# Ingrese una clave: 123456
# Clave invalida
# Ingrese una clave: 1234567
# Clave invalida
# Ingrese una clave: 1234567a
# Clave valida


# Ejercicio 2: Cree un programa que le pida cree un numero aleatorio, dejelo guardado en una variable y asuma que el usuario no sabe el numero, pida al usuario que adivine el numero, si el usuario falla se le debe indicar si el numero es mayor o menor al numero aleatorio, si el usuario adivina el numero imprima 'Felicidades adivinaste el numero' y termine el programa.

# Ejemplo:
# Numero aleatorio: 5
# Adivina el numero: 3
# El numero es mayor
# Adivina el numero: 7
# El numero es menor
# Adivina el numero: 5
# Felicidades adivinaste el numero


